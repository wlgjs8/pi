@dataclasses.dataclass(frozen=True)
class ImageTransformConfig:
    """Weak, label-preserving image augmentation knobs (train-only).

    Only photometric effects + a small random-resized-crop are used, so the
    image->action correspondence is preserved (safe for the ego-centric relative
    policy). Shadow direction is randomized (covers the mirrored-shadow case between
    two facing collection/inference sites) WITHOUT flipping the image, which would
    break left/right arm semantics and rotation chirality. Defaults are intentionally
    mild."""

    # Tier 2: small random-resized-crop (area scale in [crop_scale_min, 1.0]).
    crop_scale_min: float = 0.9
    crop_prob: float = 0.5
    # Tier 1: photometric color jitter.
    brightness: float = 0.2
    contrast: float = 0.2
    saturation: float = 0.2
    hue: float = 0.03
    photometric_prob: float = 0.8
    gaussian_noise_std: float = 3.0  # on the 0-255 scale
    noise_prob: float = 0.3
    # Weak directional motion blur.
    motion_blur_max_ksize: int = 5
    motion_blur_prob: float = 0.25
    # Weak random-direction linear-gradient shadow (mirror-symmetric directions included).
    shadow_strength: float = 0.25
    shadow_prob: float = 0.3
    # Image keys to leave untouched (e.g. the zero-filled placeholder camera).
    skip_keys: tuple[str, ...] = ("base_0_rgb",)


@dataclasses.dataclass(frozen=True)
class AugmentImages(DataTransformFn):
    """Train-only per-camera image augmentation. Operates on uint8 HWC images in
    ``data["image"]`` (after the policy input transform builds the image dict, before
    ResizeImages). Injected only in the training data pipeline, so inference and
    norm-stat computation are unaffected."""

    cfg: ImageTransformConfig

    def __call__(self, data: DataDict) -> DataDict:
        images = data.get("image")
        if not isinstance(images, Mapping):
            return data
        out = {}
        for k, v in images.items():
            img = np.asarray(v)
            if k in self.cfg.skip_keys or img.ndim != 3 or img.size == 0 or int(img.max()) == 0:
                out[k] = v
            else:
                out[k] = self._augment_one(img.astype(np.uint8))
        data["image"] = out
        return data

    def _augment_one(self, img: np.ndarray) -> np.ndarray:
        import cv2

        c = self.cfg
        h, w = img.shape[:2]
        if c.crop_scale_min < 1.0 and np.random.rand() < c.crop_prob:
            scale = np.random.uniform(c.crop_scale_min, 1.0)
            ch = max(1, int(round(h * np.sqrt(scale))))
            cw = max(1, int(round(w * np.sqrt(scale))))
            y0 = np.random.randint(0, h - ch + 1)
            x0 = np.random.randint(0, w - cw + 1)
            img = cv2.resize(img[y0 : y0 + ch, x0 : x0 + cw], (w, h), interpolation=cv2.INTER_LINEAR)
        if np.random.rand() < c.photometric_prob:
            img = self._photometric(img, cv2)
        if c.gaussian_noise_std > 0 and np.random.rand() < c.noise_prob:
            noise = np.random.normal(0.0, c.gaussian_noise_std, img.shape)
            img = np.clip(img.astype(np.float32) + noise, 0, 255).astype(np.uint8)
        if c.motion_blur_max_ksize >= 3 and np.random.rand() < c.motion_blur_prob:
            img = self._motion_blur(img, cv2)
        if c.shadow_strength > 0 and np.random.rand() < c.shadow_prob:
            img = self._shadow(img)
        return img

    def _photometric(self, img: np.ndarray, cv2) -> np.ndarray:
        c = self.cfg
        x = img.astype(np.float32)
        if c.brightness > 0:
            x *= 1.0 + np.random.uniform(-c.brightness, c.brightness)
        if c.contrast > 0:
            m = float(x.mean())
            x = (x - m) * (1.0 + np.random.uniform(-c.contrast, c.contrast)) + m
        x = np.clip(x, 0, 255).astype(np.uint8)
        if c.saturation > 0 or c.hue > 0:
            hsv = cv2.cvtColor(x, cv2.COLOR_RGB2HSV).astype(np.float32)
            if c.saturation > 0:
                hsv[..., 1] *= 1.0 + np.random.uniform(-c.saturation, c.saturation)
            if c.hue > 0:
                hsv[..., 0] += np.random.uniform(-c.hue, c.hue) * 180.0
            hsv[..., 0] = np.mod(hsv[..., 0], 180.0)
            hsv[..., 1] = np.clip(hsv[..., 1], 0, 255)
            x = cv2.cvtColor(hsv.astype(np.uint8), cv2.COLOR_HSV2RGB)
        return x

    def _motion_blur(self, img: np.ndarray, cv2) -> np.ndarray:
        k = int(np.random.randint(3, self.cfg.motion_blur_max_ksize + 1))
        kernel = np.zeros((k, k), np.float32)
        kernel[k // 2, :] = 1.0 / k
        rot = cv2.getRotationMatrix2D((k / 2 - 0.5, k / 2 - 0.5), float(np.random.uniform(0, 180)), 1.0)
        kernel = cv2.warpAffine(kernel, rot, (k, k))
        s = float(kernel.sum())
        if s > 1e-6:
            kernel /= s
        return cv2.filter2D(img, -1, kernel)

    def _shadow(self, img: np.ndarray) -> np.ndarray:
        c = self.cfg
        h, w = img.shape[:2]
        ang = float(np.random.uniform(0, 2 * np.pi))
        yy, xx = np.mgrid[0:h, 0:w].astype(np.float32)
        grad = np.cos(ang) * (xx / w) + np.sin(ang) * (yy / h)
        grad = (grad - grad.min()) / (grad.max() - grad.min() + 1e-6)
        strength = float(np.random.uniform(0, c.shadow_strength))
        factor = (1.0 - strength * grad)[..., None]
        return np.clip(img.astype(np.float32) * factor, 0, 255).astype(np.uint8)


